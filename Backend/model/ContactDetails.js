const mongoose = require("mongoose");

//schema
const userSchema = new mongoose.Schema({
  name: {
    type: String,
    required: true,
  },
  email: {
    type: String,
    required: true,
  },
  mobile: {
    type: String,
    required: true,
  },
  message: {
    type: String,
    required: true,
  },
});

//model
const USER = mongoose.model("USER", userSchema);
module.exports = USER;
